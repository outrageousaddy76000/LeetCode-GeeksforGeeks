class Solution
{
    public:
    //Function to remove duplicates from unsorted linked list.
    Node * removeDuplicates( Node *head) 
    {
        unordered_map <int,int> m,vis;
        struct Node *ptr = head;
        while(ptr!=NULL){
            m[ptr->data]++;
            vis[ptr->data]=0;
            ptr=ptr->next;
        }
        ptr=head;
        vis[ptr->data]=1;
        while(ptr!=NULL && ptr->next!=NULL){
            if(m[(ptr->next)->data]>1 && vis[(ptr->next)->data]==1){
                //delete ptr->next
                ptr->next=(ptr->next)->next;
                continue;
            }
            else if(m[ptr->next->data]>1){
                vis[ptr->next->data]=1;
            }
            ptr=ptr->next;
        }
        return head;
    }
};


